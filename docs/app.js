// --- Accordion ---
function renderAccordion(data, sourceMap) {
  if (!data || !data.items) return '';
  var html = '<div class="accordion">';
  for (var i = 0; i < data.items.length; i++) {
    var item = data.items[i];
    var openCls = (i === 0 && item.open !== false) || item.open ? ' open' : '';
    html += '<div class="accordion-item' + openCls + '">';
    html += '<div class="accordion-header">';
    html += '<div class="accordion-header-left">';
    html += '<h3>' + item.title + '</h3>';
    if (item.badges) {
      for (var b = 0; b < item.badges.length; b++) {
        html += renderBadge(item.badges[b]);
      }
    }
    html += '</div>';
    html += '<span class="accordion-arrow">&#9660;</span>';
    html += '</div>';
    html += '<div class="accordion-content"><div class="accordion-body">';
    if (item.content) {
      if (item.content.summary) {
        html += '<p>' + resolveCitations(item.content.summary, sourceMap) + '</p>';
      }
      if (item.content.entries && item.content.entries.length) {
        html += '<ul class="criteria-list">';
        for (var e = 0; e < item.content.entries.length; e++) {
          var entry = item.content.entries[e];
          html += '<li>' + resolveCitations(entry.text, sourceMap);
          if (entry.source) {
            if (entry.url) {
              html += ' <span class="criteria-source">(<a href="' + entry.url + '" target="_blank" class="source-link">' + entry.source + '</a>)</span>';
            } else {
              html += ' <span class="criteria-source">(' + entry.source + ')</span>';
            }
          }
          html += '</li>';
        }
        html += '</ul>';
      }
    }
    html += '</div></div></div>';
  }
  html += '</div>';
  return html;
}

// --- Bar Chart ---
function renderBarChart(data) {
  if (!data || !data.bars) return '';
  var html = '';
  if (data.title) {
    html += '<h3 style="margin-top:3rem;">' + data.title + '</h3>';
  }
  html += '<div class="bar-chart">';
  for (var i = 0; i < data.bars.length; i++) {
    var bar = data.bars[i];
    var bgStyle = 'background: var(--gradient-1);';
    if (bar.color === 'green') bgStyle = 'background: var(--green);';
    else if (bar.color === 'amber') bgStyle = 'background: linear-gradient(135deg, #fbbf24 0%, #f59e0b 100%);';
    else if (bar.color === 'gray') bgStyle = 'background: linear-gradient(135deg, #71717a 0%, #52525b 100%);';
    else if (bar.color === 'red') bgStyle = 'background: var(--red);';
    html += '<div class="bar-row">';
    html += '<div class="bar-label">' + bar.label + '</div>';
    html += '<div class="bar-track"><div class="bar-fill" style="width:' + bar.percent + '%;' + bgStyle + '">' + (bar.text || bar.percent + '%') + '</div></div>';
    html += '</div>';
  }
  html += '</div>';
  return html;
}

// --- Gate Review ---
function renderGateReview(data, sourceMap) {
  if (!data) return '';
  var html = '';

  // Evidence briefing
  if (data.briefing) {
    html += '<div class="evidence-briefing">';
    html += '<h3>Evidence Briefing</h3>';
    if (data.briefing.stats) {
      html += '<div class="briefing-stats">';
      for (var i = 0; i < data.briefing.stats.length; i++) {
        var bs = data.briefing.stats[i];
        html += '<div class="briefing-stat">';
        html += '<span class="briefing-stat-value">' + bs.value + '</span>';
        html += '<span class="briefing-stat-label">' + bs.label + '</span>';
        html += '</div>';
      }
      html += '</div>';
    }
    html += '<div class="briefing-columns">';
    if (data.briefing.strengths) {
      html += '<div class="briefing-column">';
      html += '<h4 class="briefing-strong">What\'s Strong</h4>';
      html += '<ul class="briefing-list strong">';
      for (var s = 0; s < data.briefing.strengths.length; s++) {
        html += '<li>' + resolveCitations(data.briefing.strengths[s], sourceMap) + '</li>';
      }
      html += '</ul></div>';
    }
    if (data.briefing.limitations) {
      html += '<div class="briefing-column">';
      html += '<h4 class="briefing-weak">What\'s Weak or Unresolved</h4>';
      html += '<ul class="briefing-list weak">';
      for (var w = 0; w < data.briefing.limitations.length; w++) {
        html += '<li>' + resolveCitations(data.briefing.limitations[w], sourceMap) + '</li>';
      }
      html += '</ul></div>';
    }
    html += '</div></div>';
  }

  // Validation card
  if (data.validation) {
    var v = data.validation;
    html += '<div class="validation-card">';
    html += '<div class="validation-status">';
    html += '<div class="validation-icon">&#10003;</div>';
    html += '<div class="validation-text">';
    html += '<h3>' + (v.title || 'Validation Passed') + '</h3>';
    html += '<p>' + (v.summary || '') + '</p>';
    html += '</div></div>';
    html += '<div class="validation-stats">';
    html += '<div class="validation-stat"><div class="validation-stat-value" style="color:var(--green);">' + (v.passed || 0) + '</div><div class="validation-stat-label">Passed</div></div>';
    html += '<div class="validation-stat"><div class="validation-stat-value" style="color:var(--amber);">' + (v.warnings || 0) + '</div><div class="validation-stat-label">Warning</div></div>';
    html += '<div class="validation-stat"><div class="validation-stat-value" style="color:var(--red);">' + (v.failed || 0) + '</div><div class="validation-stat-label">Failed</div></div>';
    html += '</div></div>';
  }

  // Gate decisions
  if (data.decisions && data.decisions.length) {
    for (var d = 0; d < data.decisions.length; d++) {
      var dec = data.decisions[d];
      var cardCls = dec.type === 'strategic' ? ' strategic' : ' blocking';
      if (d === 0 || data.decisions[d - 1].type !== dec.type) {
        var groupLabel = dec.type === 'blocking' ? 'Blocking Decision' : 'Strategic Decisions';
        var groupDesc = dec.type === 'blocking'
          ? 'Must be resolved before the next workspace can start.'
          : 'Worth discussing but not blocking.';
        html += '<h3 class="gate-group-label">' + groupLabel + '</h3>';
        html += '<p class="gate-group-desc">' + groupDesc + '</p>';
        html += '<div class="gate-cards">';
      }
      html += '<div class="gate-card' + cardCls + '">';
      html += '<div class="gate-card-header">';
      html += '<div class="gate-num">' + dec.num + '</div>';
      html += '<div class="gate-question">' + dec.question + '</div>';
      if (dec.type) {
        html += '<span class="gate-priority ' + dec.type + '">' + dec.type.charAt(0).toUpperCase() + dec.type.slice(1) + '</span>';
      }
      html += '</div>';
      if (dec.context) {
        html += '<p class="gate-context">' + resolveCitations(dec.context, sourceMap) + '</p>';
      }
      if (dec.options && dec.options.length) {
        html += '<div class="gate-options">';
        for (var o = 0; o < dec.options.length; o++) {
          var opt = dec.options[o];
          html += '<div class="gate-option"><strong>' + opt.label + '</strong>';
          if (opt.consequence) {
            html += ' <span class="gate-consequence">&rarr; ' + opt.consequence + '</span>';
          }
          html += '</div>';
        }
        html += '</div>';
      }
      html += '</div>';
      var isLast = (d === data.decisions.length - 1);
      var nextDifferent = !isLast && data.decisions[d + 1].type !== dec.type;
      if (isLast || nextDifferent) {
        html += '</div>';
      }
    }
  }

  return html;
}

// --- Sources ---
function renderSources(data) {
  if (!data || !data.categories) return '';
  var html = '<div class="sources-grid">';
  for (var i = 0; i < data.categories.length; i++) {
    var cat = data.categories[i];
    html += '<div class="source-category">';
    html += '<h3>' + cat.name + '</h3>';
    html += '<ul class="source-list">';
    for (var j = 0; j < cat.items.length; j++) {
      var item = cat.items[j];
      html += '<li>';
      html += '<span class="source-org">' + item.org + '</span>';
      if (item.date) {
        html += ' <span class="source-date">(' + item.date + ')</span>';
      }
      html += ' &mdash; ';
      if (item.url) {
        html += '<a href="' + item.url + '" target="_blank">' + item.title + '</a>';
      } else {
        html += item.title;
      }
      if (item.cited_for) {
        html += ' <span class="criteria-source">[' + item.cited_for + ']</span>';
      }
      html += '</li>';
    }
    html += '</ul></div>';
  }
  html += '</div>';
  if (data.note) {
    html += '<p style="font-size:0.8rem;color:var(--text-muted);margin-top:1.5rem;font-style:italic;">' + data.note + '</p>';
  }
  return html;
}

