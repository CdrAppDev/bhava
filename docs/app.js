function renderNav(projectName, title, navLinks, hasWorkspaceNav) {
  var topStyle = hasWorkspaceNav ? ' style="top: 2rem;"' : '';
  var html = '<nav' + topStyle + '>';
  html += '<a href="#" class="nav-logo">' + (title || projectName) + '</a>';
  if (navLinks && navLinks.length) {
    html += '<ul class="nav-links">';
    for (var i = 0; i < navLinks.length; i++) {
      html += '<li><a href="' + navLinks[i].href + '">' + navLinks[i].label + '</a></li>';
    }
    html += '</ul>';
  }
  html += '</nav>';
  return html;
}

function renderWorkspaceNav(wsNav, projectName) {
  var html = '<div class="workspace-nav" style="position:fixed;top:0;left:0;right:0;z-index:200;';
  html += 'background:var(--bg-secondary);border-bottom:1px solid var(--border);';
  html += 'padding:0.5rem 2rem;display:flex;align-items:center;gap:1.5rem;font-size:0.8rem;">';
  html += '<a href="index.html" style="color:var(--text-primary);text-decoration:none;';
  html += 'font-weight:700;font-size:0.85rem;">' + (projectName || 'Home') + '</a>';
  if (wsNav.links) {
    for (var i = 0; i < wsNav.links.length; i++) {
      var link = wsNav.links[i];
      var disabledStyle = link.disabled
        ? 'color:var(--text-muted);pointer-events:none;'
        : 'color:var(--accent-light);';
      html += '<a href="' + link.href + '" style="text-decoration:none;font-weight:500;';
      html += disabledStyle + '">' + link.label + '</a>';
    }
  }
  html += '</div>';
  return html;
}


