/* ====================================================================
   4. SECTION ASSEMBLER
   ==================================================================== */

function renderSection(section, sourceMap, evidencePopups) {
  var altClass = section.alt ? ' section-alt' : '';
  var html = '<section id="' + section.id + '" class="' + altClass + '">';
  html += '<div class="container">';

  if (section.section_num) {
    html += '<div class="section-num">' + section.section_num + ' / ' + section.section_title + '</div>';
  }
  if (section.headline) {
    html += '<h2>' + section.headline + '</h2>';
  }
  if (section.lead) {
    html += '<p class="section-lead">' + resolveCitations(section.lead, sourceMap, evidencePopups) + '</p>';
  }

  // Component can be compound: "stat-row + card-grid-2 + bar-chart"
  var components = section.component.split(' + ');
  for (var i = 0; i < components.length; i++) {
    var compName = components[i].trim();
    var renderer = RENDERERS[compName];
    if (renderer) {
      // For compound components, look for sub-key (hyphens to underscores)
      var dataKey = compName.replace(/-/g, '_');
      var compData = (components.length > 1 && section.data[dataKey])
        ? section.data[dataKey]
        : section.data;
      html += renderer(compData, sourceMap, evidencePopups);
    }
  }

  html += '</div></section>';
  return html;
}


