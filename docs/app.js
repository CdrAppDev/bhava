/* ====================================================================
   1. CITATION ENGINE
   ==================================================================== */

function resolveCitations(text, sourceMap, evidencePopups) {
  if (!text) return '';

  // Resolve evidence markers: <<evidence:id>>text<</evidence>>
  text = text.replace(/<<evidence:([\w-]+)>>([\s\S]*?)<\/evidence>>/g, function(_, id, claim) {
    return '<span class="evidence-trigger" data-evidence-id="' + id + '">' + claim + '</span>';
  });

  // Resolve citation markers: {{Source Name, Date}}
  text = text.replace(/\{\{([^}]+)\}\}/g, function(_, ref) {
    var url = sourceMap ? sourceMap[ref.trim()] : null;
    if (url) {
      return '(<a href="' + url + '" target="_blank" class="source-link">' + ref.trim() + '</a>)';
    }
    return '(' + ref.trim() + ')';
  });

  // Resolve bold markers: **text**
  text = text.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');

  return text;
}


