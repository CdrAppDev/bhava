// --- Filter Table ---
function renderFilterTable(data) {
  if (!data) return '';
  var html = '';
  if (data.filters && data.filters.length) {
    html += '<div class="filter-tabs">';
    for (var f = 0; f < data.filters.length; f++) {
      var flt = data.filters[f];
      var activeCls = f === 0 ? ' active' : '';
      html += '<button class="tab-btn' + activeCls + '" data-filter="' + flt.value + '">';
      html += flt.label + ' (' + flt.count + ')</button>';
    }
    html += '</div>';
  }
  if (data.headers && data.rows) {
    html += '<div class="table-wrap"><table class="data-table"';
    if (data.table_id) html += ' id="' + data.table_id + '"';
    html += '>';
    html += '<thead><tr>';
    for (var h = 0; h < data.headers.length; h++) {
      html += '<th>' + data.headers[h] + '</th>';
    }
    html += '</tr></thead><tbody>';
    for (var r = 0; r < data.rows.length; r++) {
      var row = data.rows[r];
      html += '<tr data-category="' + (row.category || '') + '">';
      for (var c = 0; c < row.cells.length; c++) {
        var cell = row.cells[c];
        if (cell && typeof cell === 'object' && cell.badge) {
          html += '<td>' + renderBadge(cell.badge) + '</td>';
        } else {
          html += '<td>' + (cell || '') + '</td>';
        }
      }
      html += '</tr>';
    }
    html += '</tbody></table></div>';
  }
  return html;
}

