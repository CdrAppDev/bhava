// --- Hero ---
function renderHero(data) {
  if (!data) return '';
  var html = '<section class="hero"><div class="container">';
  html += '<h1>' + (data.title_line1 || '') + '<br><span class="gradient-text">' + (data.title_line2 || '') + '</span></h1>';
  if (data.lead) {
    html += '<p class="lead">' + data.lead + '</p>';
  }
  if (data.funnel && data.funnel.length) {
    html += '<div class="funnel">';
    for (var i = 0; i < data.funnel.length; i++) {
      if (i > 0) html += '<span class="funnel-arrow">&rarr;</span>';
      var cls = data.funnel[i].highlight ? ' highlight' : '';
      html += '<div class="funnel-box' + cls + '">' + data.funnel[i].label + '</div>';
    }
    html += '</div>';
  }
  if (data.stats && data.stats.length) {
    html += '<div class="hero-stats">';
    for (var s = 0; s < data.stats.length; s++) {
      var stat = data.stats[s];
      var colorClass = stat.color ? ' ' + stat.color : '';
      html += '<div>';
      html += '<div class="hero-stat-value' + colorClass + '">' + stat.value + '</div>';
      html += '<div class="hero-stat-label">' + stat.label + '</div>';
      html += '</div>';
    }
    html += '</div>';
  }
  html += '</div></section>';
  return html;
}

// --- Narrative ---
function renderNarrative(data, sourceMap, evidencePopups) {
  if (!data || !data.chapters) return '';
  var html = '';
  for (var i = 0; i < data.chapters.length; i++) {
    if (i > 0) html += '<div class="narrative-divider"></div>';
    var ch = data.chapters[i];
    html += '<div class="narrative-chapter">';
    html += '<div class="narrative-num">Chapter ' + (i + 1) + ' &mdash; ' + ch.label + '</div>';
    html += '<h3>' + resolveCitations(ch.headline, sourceMap, evidencePopups) + '</h3>';
    if (ch.body) {
      for (var p = 0; p < ch.body.length; p++) {
        html += '<p>' + resolveCitations(ch.body[p], sourceMap, evidencePopups) + '</p>';
      }
    }
    html += '</div>';
  }
  if (data.cta) {
    html += '<div class="narrative-cta"><strong>' + data.cta.emphasis + '</strong> ' + data.cta.text + '</div>';
  }
  return html;
}

function renderNarrativeSection(data, sourceMap, evidencePopups) {
  return '<section class="narrative" id="story"><div class="container">' +
    renderNarrative(data, sourceMap, evidencePopups) +
    '</div></section>';
}

// --- Card Grid ---
function renderCardGrid(data, sourceMap) {
  if (!data || !data.cards) return '';
  var cols = data.cols || 2;
  var html = '<div class="card-grid cols-' + cols + '">';
  for (var i = 0; i < data.cards.length; i++) {
    var card = data.cards[i];
    html += '<div class="card">';
    if (card.label) {
      var labelColor = card.label.color || '';
      html += '<div class="card-label ' + labelColor + '">' + card.label.text + '</div>';
    }
    if (card.title) {
      if (card.badges && card.badges.length) {
        html += '<div style="display:flex;align-items:center;gap:0.75rem;margin-bottom:0.5rem;">';
        html += '<h3 style="margin:0;">' + resolveCitations(card.title, sourceMap) + '</h3>';
        for (var b = 0; b < card.badges.length; b++) {
          html += renderBadge(card.badges[b]);
        }
        html += '</div>';
      } else {
        html += '<h3>' + resolveCitations(card.title, sourceMap) + '</h3>';
      }
    }
    if (card.body) {
      html += '<p>' + resolveCitations(card.body, sourceMap) + '</p>';
    }
    if (card.extra_html) {
      html += card.extra_html;
    }
    html += '</div>';
  }
  html += '</div>';
  return html;
}

