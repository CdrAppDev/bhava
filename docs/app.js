// --- Workspace Page ---
function renderWorkspacePage(data) {
  var sourceMap = data.sources ? data.sources.source_map || {} : {};
  var evidencePopups = data.evidence_popups || {};
  var hasWsNav = !!data.workspace_nav;
  var html = '';

  if (data.workspace_nav) {
    html += renderWorkspaceNav(data.workspace_nav, data.project_name);
  }

  html += renderNav(data.project_name, data.title, data.nav_links, hasWsNav);

  // Hero with extra padding when workspace nav present
  if (data.hero) {
    if (hasWsNav) {
      html += '<style>.hero { padding-top: 12rem; } nav { top: 2rem; }</style>';
    }
    html += renderHero(data.hero);
  }

  // Narrative
  if (data.narrative) {
    html += renderNarrativeSection(data.narrative, sourceMap, evidencePopups);
  }

  // Numbered sections
  if (data.sections) {
    for (var i = 0; i < data.sections.length; i++) {
      html += renderSection(data.sections[i], sourceMap, evidencePopups);
    }
  }

  // Footer
  html += renderFooter(data.footer);

  // Inject evidence modal if needed
  if (evidencePopups && Object.keys(evidencePopups).length > 0) {
    injectEvidenceModal();
  }

  var appEl = document.getElementById('app');
  appEl.className = '';  /* Remove .loading class — stops flex layout */
  setHTML(appEl, html);
  document.title = (data.project_name || '') + ' \u2014 ' + (data.title || '');
}

// --- Mini Pipeline (for branch cards) ---
function renderMiniPipeline(pipeline) {
  if (!pipeline || !pipeline.length) return '';
  var html = '<div class="pipeline-mini">';
  for (var i = 0; i < pipeline.length; i++) {
    var node = pipeline[i];
    var dotCls = node.state || '';
    var icon = '';
    if (dotCls === 'complete') icon = '&#10003;';
    else if (dotCls === 'killed') icon = '&#10007;';
    else if (dotCls === 'active') icon = '&#9679;';
    else icon = '';

    html += '<div class="pipeline-mini-node">';
    html += '<div class="pipeline-mini-dot ' + dotCls + '">' + icon + '</div>';
    html += '<span class="pipeline-mini-label ' + dotCls + '">' + node.label + '</span>';
    html += '</div>';

    if (i < pipeline.length - 1) {
      var lineCls = node.state === 'complete' ? 'complete' : '';
      html += '<div class="pipeline-mini-line ' + lineCls + '"></div>';
    }
  }
  html += '</div>';
  return html;
}

