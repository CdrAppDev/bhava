/* ====================================================================
   8. INTERACTIVE BEHAVIORS — all event-delegated
   ==================================================================== */

function initInteractivity(data) {
  // Smooth scroll for anchor links
  document.addEventListener('click', function(e) {
    var link = e.target.closest('a[href^="#"]');
    if (!link) return;
    var href = link.getAttribute('href');
    if (href === '#') return;
    var target = document.querySelector(href);
    if (target) {
      e.preventDefault();
      target.scrollIntoView({ behavior: 'smooth' });
    }
  });

  // Accordion toggle
  document.addEventListener('click', function(e) {
    var header = e.target.closest('.accordion-header');
    if (!header) return;
    header.closest('.accordion-item').classList.toggle('open');
  });

  // Filter tabs
  document.addEventListener('click', function(e) {
    var btn = e.target.closest('.tab-btn');
    if (!btn) return;
    var filter = btn.dataset.filter;
    var section = btn.closest('section');
    section.querySelectorAll('.tab-btn').forEach(function(b) {
      b.classList.remove('active');
    });
    btn.classList.add('active');
    section.querySelectorAll('.data-table tbody tr').forEach(function(row) {
      row.style.display = (filter === 'all' || row.dataset.category === filter) ? '' : 'none';
    });
  });

  // Evidence popup
  var evidencePopups = data.evidence_popups || {};
  if (evidencePopups && Object.keys(evidencePopups).length > 0) {
    var modal = document.getElementById('evidence-modal');
    if (!modal) return;

    document.addEventListener('click', function(e) {
      var trigger = e.target.closest('.evidence-trigger');
      if (trigger) {
        var id = trigger.dataset.evidenceId;
        var ev = evidencePopups[id];
        if (!ev) return;
        document.getElementById('evidence-modal-claim').textContent = ev.claim;
        var chain = document.getElementById('evidence-chain');
        setHTML(chain, ev.chain.map(function(item) {
          return '<li><span class="evidence-finding">' + item.finding + '</span>' +
            '<span class="evidence-source">' +
            (item.url
              ? '<a href="' + item.url + '" target="_blank">' + item.source + '</a>'
              : item.source) +
            ' <span class="evidence-date">(' + item.date + ')</span></span></li>';
        }).join(''));
        document.getElementById('evidence-modal-conclusion').textContent = ev.conclusion;
        modal.classList.add('active');
        return;
      }
      // Close on backdrop or close button
      if (e.target.closest('.evidence-modal-close') || e.target === modal) {
        modal.classList.remove('active');
      }
    });

    // Close on Escape
    document.addEventListener('keydown', function(e) {
      if (e.key === 'Escape') {
        modal.classList.remove('active');
      }
    });
  }
}


/* ====================================================================
   9. BOOTSTRAP — DOMContentLoaded entry point
   ==================================================================== */

document.addEventListener('DOMContentLoaded', function() {
  var pageType = document.body.dataset.pageType;
  var workspace = document.body.dataset.workspace;

  var jsonFile;
  if (pageType === 'index') {
    jsonFile = 'venture.json';
  } else if (pageType === 'traceability') {
    jsonFile = 'traceability.json';
  } else {
    jsonFile = workspace + '.json';
  }

  // Support inline data (file:// protocol) or fetch (http/https)
  var dataPromise;
  if (window.__VF_DATA__) {
    dataPromise = Promise.resolve(window.__VF_DATA__);
  } else {
    dataPromise = fetch(jsonFile)
      .then(function(r) {
        if (!r.ok) throw new Error('HTTP ' + r.status + ' loading ' + jsonFile);
        return r.json();
      });
  }

  dataPromise
    .then(function(data) {
      if (pageType === 'index') {
        renderIndexPage(data);
      } else if (pageType === 'traceability') {
        renderTraceabilityPage(data);
      } else {
        renderWorkspacePage(data);
        initInteractivity(data);
      }
    })
    .catch(function(err) {
      setHTML(document.getElementById('app'),
        '<div style="padding:4rem;text-align:center;color:var(--text-muted);">' +
        '<h2>Failed to load presentation</h2><p>' + err.message + '</p></div>'
      );
    });
});
